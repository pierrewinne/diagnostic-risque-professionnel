export type NormativeFamily = 'luxembourg' | 'iso' | 'apsad' | 'nfpa' | 'vds'

export type NormativeStatus = 'mandatory' | 'conditional' | 'voluntary'

export interface NormativeReference {
  /** Identifiant unique (ex: "LU-RGD-2018-08-17", "ISO-45001", "APSAD-R7") */
  id: string
  /** Famille normative */
  family: NormativeFamily
  /** Catégories de risque couvertes */
  categories: RiskCategory[]
  /** Numéro/référence officielle */
  reference: string
  /** Titre complet */
  title: string
  /** Année de la version en vigueur */
  year: number
  /** Organisme émetteur */
  issuer: string
  /** Domaine couvert (résumé court) */
  scope: string
  /** Exigences clés (liste structurée) */
  keyRequirements: string[]
  /** Statut d'applicabilité */
  status: NormativeStatus
  /** Qui est concerné */
  applicability: string
  /** Lien avec la couverture d'assurance */
  insuranceImpact: string
  /** Équivalences dans les autres référentiels */
  equivalences: string[]
  /** Lien vers le texte officiel (si disponible) */
  officialUrl?: string
  /** Certifiable ? */
  certifiable: boolean
  /** Tags pour la recherche */
  tags: string[]
}

