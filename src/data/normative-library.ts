/** Filtrer par catégorie de risque */
export function getNormsByCategory(category: RiskCategory): NormativeReference[] {
  return NORMATIVE_LIBRARY.filter(n => n.categories.includes(category))
}

/** Filtrer par famille normative */
export function getNormsByFamily(family: NormativeFamily): NormativeReference[] {
  return NORMATIVE_LIBRARY.filter(n => n.family === family)
}

/** Filtrer par statut */
export function getNormsByStatus(status: NormativeStatus): NormativeReference[] {
  return NORMATIVE_LIBRARY.filter(n => n.status === status)
}

/** Filtrer les normes certifiables */
export function getCertifiableNorms(): NormativeReference[] {
  return NORMATIVE_LIBRARY.filter(n => n.certifiable)
}

/** Recherche plein texte dans la bibliothèque */
export function searchNorms(query: string): NormativeReference[] {
  const lower = query.toLowerCase()
  return NORMATIVE_LIBRARY.filter(n =>
    n.reference.toLowerCase().includes(lower) ||
    n.title.toLowerCase().includes(lower) ||
    n.scope.toLowerCase().includes(lower) ||
    n.tags.some(t => t.toLowerCase().includes(lower)) ||
    n.keyRequirements.some(r => r.toLowerCase().includes(lower))
  )
}

